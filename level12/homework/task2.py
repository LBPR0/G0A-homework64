# ოპერატორი (operator) არის სიმბოლო, რომელიც განსაზღვრავს მოქმედებას (მაგ., >, <, ==).
# ოპერაცია (operation) არის კონკრეტული მოქმედება, რომელიც შესრულებულია ოპერატორის გამოყენებით (მაგ., 5 > 3).

result1 = 5 > 3  # True: 5 მეტია 3-ზე
result2 = 7 <= 7  # True: 7 ნაკლებია ან ტოლია 7-ის
result3 = -1 == -1  # True: -1 ტოლია -1-ის
result4 = 10 != 5  # True: 10 არ არის ტოლი 5-ის
result5 = 2 >= 8  # False: 2 არ არის მეტი ან ტოლი 8-ის

