# Integer (მთელი რიცხვი) არის მონაცემთა ტიპი, რომელიც გამოიყენება მთელი რიცხვების შესანახად.
# მაგალითად: 5, -10, 0

# Float (მცურავი წერტილიანი რიცხვი) არის მონაცემთა ტიპი, რომელიც გამოიყენება ათწილადი რიცხვების შესანახად.
# მაგალითად: 5.5, -10.75, 0.0

# ძირითადი განსხვავება:
# Integer არ შეიცავს ათწილად ნაწილს, ხოლო Float შეიცავს.
# Float გამოიყენება მაშინ, როდესაც საჭიროა ათწილადი რიცხვების ან უფრო მაღალი სიზუსტის მონაცემების შენახვა.