
number = int(input("შეიყვანეთ რიცხვი: "))

if number > 0:
    print("positive")  
elif number < 0:
    print("negative") 
else:
    print("neutral")  

# განსხვავება if-სა და elif-ს შორის:
#  `if` გამოიყენება დამოუკიდებელი პირობის შესამოწმებლად.
#  ყველა `if` პირობა შემოწმდება, მიუხედავად იმისა, შესრულდა თუ არა წინა პირობა.
# `elif` გამოიყენება მხოლოდ მაშინ, როცა წინა `if` პირობა არ შესრულდა.
#   თუ `if` პირობა შესრულდა, `elif` პირობა აღარ შემოწმდება.
