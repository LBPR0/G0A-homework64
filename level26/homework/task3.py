# lower() მეთოდი აბრუნებს სტრიქონს, სადაც ყველა ასო გადაყვანილია პატარა ასოებში
text = "Hello World"
print(text.lower())  # შედეგი: "hello world"

# upper() მეთოდი აბრუნებს სტრიქონს, სადაც ყველა ასო გადაყვანილია დიდი ასოებში
print(text.upper())  # შედეგი: "HELLO WORLD"

# capitalize() მეთოდი აბრუნებს სტრიქონს, სადაც მხოლოდ პირველი ასოა დიდი, დანარჩენი პატარა
print(text.capitalize())  # შედეგი: "Hello world"