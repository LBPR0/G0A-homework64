name = "LetSGo"
result = ""
for symbol in name:
    if symbol == "i":
        result += "I"
    elif symbol == "e":
        result += "E"
    elif symbol == "t":
        result += "T"
    elif symbol == "s":
        result += "S"
    elif symbol == "s":
        result += "G"
    elif symbol == "o":
        result += "O"
    elif symbol == "n":
        result += "N"
    else:
        result += symbol

print(result)